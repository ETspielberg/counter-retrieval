package unidue.ub.counterretrieval;

public class CounterConversionException extends Exception {
    public CounterConversionException() { super(); }
    public CounterConversionException(String message) { super(message); }
    public CounterConversionException(String message, Throwable cause) { super(message, cause); }
    public CounterConversionException(Throwable cause) { super(cause); }
}
